    /// <summary>
    /// Compile a SpellDna into a SpellTuningPreset.
    ///
    /// The DNA document is the design spec; the tuning preset is its
    /// compiled output. This bridges Tier 3 → the existing tuning system.
    ///
    /// Each SpellElement with emitter parameter overrides becomes an
    /// EmitterPatch in the preset. The "why" annotations are stripped
    /// since the tuning system doesn't use them.
    /// </summary>
    public SpellTuningPreset? CompileToPreset(SpellDna dna)
    {
        if (dna.Phases == null || dna.Phases.Count == 0) return null;

        var preset = new SpellTuningPreset
        {
            PresetName = $"{dna.SpellName}_dna_v{dna.Version}",
            SourceSpellEntry = dna.SourceSpellEntry,
            Theme = dna.Theme,
            Phases = new Dictionary<string, PhaseTuning>()
        };

        foreach (var (phaseKey, phaseDna) in dna.Phases)
        {
            var phaseTuning = new PhaseTuning
            {
                SourceM2 = phaseDna.SourceM2,
                Emitters = new List<EmitterPatch>(),
                Textures = new List<TextureTuning>()
            };

            if (phaseDna.Elements != null)
            {
                foreach (var element in phaseDna.Elements)
                {
                    // Build EmitterPatch from element's parameter overrides
                    if (element.Params != null && element.Params.Count > 0)
                    {
                        var patch = new EmitterPatch
                        {
                            EmitterIndex = element.EmitterIndex
                        };

                        foreach (var (paramName, annotated) in element.Params)
                        {
                            switch (paramName)
                            {
                                case "emissionRate": patch.EmissionRate = annotated.Value; break;
                                case "lifespan": patch.Lifespan = annotated.Value; break;
                                case "emissionSpeed": patch.EmissionSpeed = annotated.Value; break;
                                case "gravity": patch.Gravity = annotated.Value; break;
                                case "emissionAreaLength": patch.EmissionAreaLength = annotated.Value; break;
                                case "emissionAreaWidth": patch.EmissionAreaWidth = annotated.Value; break;
                                case "verticalRange": patch.VerticalRange = annotated.Value; break;
                                case "horizontalRange": patch.HorizontalRange = annotated.Value; break;
                                case "speedVariation": patch.SpeedVariation = annotated.Value; break;
                                case "scaleStart": patch.ScaleStart = annotated.Value; break;
                                case "scaleMid": patch.ScaleMid = annotated.Value; break;
                                case "scaleEnd": patch.ScaleEnd = annotated.Value; break;
                            }
                        }

                        phaseTuning.Emitters.Add(patch);
                    }

                    // Build TextureTuning from element's texture spec
                    if (element.Texture != null && !string.IsNullOrEmpty(element.Texture.SourcePng))
                    {
                        phaseTuning.Textures.Add(new TextureTuning
                        {
                            SlotIndex = element.Texture.TextureIndex,
                            SourcePng = element.Texture.SourcePng,
                            Role = element.Texture.Role ?? "Shape",
                            Density = element.Texture.Density,
                            FloorPercent = element.Texture.FloorPercent,
                            KneeWidth = element.Texture.KneeWidth,
                        });
                    }
                }
            }

            // Only add phase if it has content
            if (phaseTuning.Emitters.Count > 0 || phaseTuning.Textures.Count > 0)
                preset.Phases[phaseKey] = phaseTuning;
        }

        return preset;
    }

    /// <summary>
    /// Scaffold a SpellDna from an M2 profile + recipe.
    /// Creates a DNA document with all emitters and textures pre-populated
    /// from the profile's data, ready for the user to add intent descriptions
    /// and "why" annotations.
    ///
    /// This is the "I just scanned this M2, now give me a starting DNA template"
    /// workflow.
    /// </summary>
    public SpellDna ScaffoldDna(string spellName, int sourceSpellEntry,
        Dictionary<string, M2Profile> phaseProfiles, SpellRecipe? recipe = null)
    {
        var dna = new SpellDna
        {
            SpellName = spellName,
            SourceSpellEntry = sourceSpellEntry,
            CreatedAt = DateTime.UtcNow,
            Version = 1,
            Phases = new Dictionary<string, DnaPhase>()
        };

        foreach (var (phaseKey, profile) in phaseProfiles)
        {
            var dnaPhase = new DnaPhase
            {
                SourceM2 = profile.M2Path,
                Description = $"(describe the visual goal for the {phaseKey} phase)",
                Elements = new List<SpellElement>()
            };

            // Get recipe phase if available
            RecipePhase? recipePhase = null;
            recipe?.Phases.TryGetValue(phaseKey, out recipePhase);

            foreach (var em in profile.Emitters)
            {
                // Find the matching recipe slot for this emitter's texture
                RecipeTextureSlot? recipeSlot = recipePhase?.Slots
                    .FirstOrDefault(s => s.Index == em.TextureId);

                var element = new SpellElement
                {
                    Name = $"emitter_{em.Index}",
                    VisualGoal = "(describe what this particle element should look like)",
                    EmitterIndex = em.Index,
                    ArchetypeId = em.ArchetypeId,
                    Params = new Dictionary<string, AnnotatedValue>()
                };

                // Pre-populate params from profile values
                if (em.EmissionRate.HasValue)
                    element.Params["emissionRate"] = new AnnotatedValue
                    { Value = em.EmissionRate.Value, Why = null };
                if (em.Lifespan.HasValue)
                    element.Params["lifespan"] = new AnnotatedValue
                    { Value = em.Lifespan.Value, Why = null };
                if (em.EmissionSpeed.HasValue)
                    element.Params["emissionSpeed"] = new AnnotatedValue
                    { Value = em.EmissionSpeed.Value, Why = null };
                if (em.Gravity.HasValue)
                    element.Params["gravity"] = new AnnotatedValue
                    { Value = em.Gravity.Value, Why = null };
                if (em.EmissionAreaLength.HasValue)
                    element.Params["emissionAreaLength"] = new AnnotatedValue
                    { Value = em.EmissionAreaLength.Value, Why = null };
                if (em.EmissionAreaWidth.HasValue)
                    element.Params["emissionAreaWidth"] = new AnnotatedValue
                    { Value = em.EmissionAreaWidth.Value, Why = null };

                element.Params["scaleStart"] = new AnnotatedValue
                { Value = em.ScaleStart, Why = null };
                element.Params["scaleMid"] = new AnnotatedValue
                { Value = em.ScaleMid, Why = null };
                element.Params["scaleEnd"] = new AnnotatedValue
                { Value = em.ScaleEnd, Why = null };

                // Texture spec from recipe or profile
                element.Texture = new ElementTexture
                {
                    TextureIndex = em.TextureId,
                    Role = recipeSlot?.Role,
                    Job = recipeSlot?.Job,
                    Density = recipeSlot?.Density,
                    VanillaFilename = profile.Textures
                        .FirstOrDefault(t => t.Index == em.TextureId)?.VanillaFilename,
                };

                dnaPhase.Elements.Add(element);
            }

            dna.Phases[phaseKey] = dnaPhase;
        }

        return dna;
    }

    // ═══════════════════════════════════════════════════════════════
    //  SEED: Default archetypes from Fireball analysis
    // ═══════════════════════════════════════════════════════════════

    private void SeedDefaultArchetypes()
    {
        var defaults = new List<EmitterArchetype>
        {
            new()
            {
                Id = "upward_drift",
