{
    /// <summary>1-based index within the batch.</summary>
    public int CloneIndex { get; set; }

    /// <summary>Name for the clone, e.g. "Fireball E03".</summary>
    public string SpellName { get; set; } = "";

    /// <summary>
    /// Goes into spell_template.description — visible in client tooltip.
    /// e.g. "[EXP #3] precast/em0: rate 40→80 | missile/em1: life 0.7→0.3"
    /// </summary>
    public string SpellDescription { get; set; } = "";

    /// <summary>One change per phase.</summary>
    public Dictionary<string, ExperimentPhaseChange> PhaseChanges { get; set; } = new();
}

/// <summary>
/// What changes in one phase of one experiment clone.
/// </summary>
public class ExperimentPhaseChange
{
    public string Phase { get; set; } = "";
    public int EmitterIndex { get; set; }
    public string Parameter { get; set; } = "";
    public float BaselineValue { get; set; }
    public float NewValue { get; set; }

    /// <summary>Human-readable change mode, e.g. "×2.0" or "=5.0".</summary>
    public string Mode { get; set; } = "";

    /// <summary>What this parameter controls.</summary>
    public string ParameterDescription { get; set; } = "";

    // ── Observation fields (filled after in-game testing) ──

    /// <summary>What did you see? Free-form notes.</summary>
    public string? Observation { get; set; }

    /// <summary>
    /// Impact rating: 0=no visible change, 1=subtle, 2=noticeable, 3=dramatic.
    /// </summary>
    public int? ImpactRating { get; set; }
}

/// <summary>
/// Post-experiment observation record.
/// The user reports what they saw for one clone after in-game testing.
/// </summary>
public class ExperimentObservation
{
    public int SourceSpellEntry { get; set; }
    public string SourceSpellName { get; set; } = "";
    public int CloneIndex { get; set; }
    public int CloneSpellEntry { get; set; }

    /// <summary>Per-phase observations.</summary>
    public Dictionary<string, PhaseObservation> Phases { get; set; } = new();

    /// <summary>Overall impression of this clone.</summary>
    public string? OverallNotes { get; set; }

    public DateTime RecordedAt { get; set; }
}

/// <summary>Observation for one phase of one experiment clone.</summary>
public class PhaseObservation
{
    public string Parameter { get; set; } = "";
    public float BaselineValue { get; set; }
    public float NewValue { get; set; }
    public string? Observation { get; set; }

    /// <summary>0=invisible, 1=subtle, 2=noticeable, 3=dramatic</summary>
    public int ImpactRating { get; set; }
}


