    /// <summary>
    /// Plan an extreme experiment batch with SMART deduplication.
    ///
    /// Instead of "pick one emitter, test all 9 params," this scans ALL
    /// emitters across ALL phases, builds a list of untested (phase, emitter,
    /// param) combos, deduplicates by baseline similarity (skip if baseline
    /// within 10% of an already-tested one for that param), and generates
    /// clones that test novel combos.
    ///
    /// Each clone still tests ONE parameter with suppress+crank extremes,
    /// but the emitter targeted CAN DIFFER per phase within a clone.
    /// For each parameter, the planner picks the most "interesting" untested
    /// emitter per phase (the one whose baseline is most different from
    /// already-tested baselines).
    ///
    /// Max 18 clones (9 params × 2 extremes). If some params have no novel
    /// combos left, fewer clones are generated.
    /// </summary>
    public ExperimentBatch PlanExtremeExperiments(
        Dictionary<string, List<EmitterSnapshot>> phaseEmitters,
        int sourceSpellEntry,
        string sourceSpellName,
        Dictionary<string, int>? targetEmitters = null)
    {
        var batch = new ExperimentBatch
        {
            SourceSpellEntry = sourceSpellEntry,
            SourceSpellName = sourceSpellName,
            CreatedAt = DateTime.UtcNow,
            Plans = new List<ExperimentPlan>()
        };

        var activePhases = phaseEmitters
            .Where(kv => kv.Value.Count > 0)
            .ToDictionary(kv => kv.Key, kv => kv.Value);

        if (activePhases.Count == 0) return batch;

        // ── Step 1: Load all previously tested combos ──
        var summary = GetSummary(sourceSpellName);
        var testedCombos = new List<TestedCombo>();
        if (summary != null)
        {
            foreach (var b in summary.Batches)
            {
                if (b.TestedCombos != null && b.TestedCombos.Count > 0)
                {
                    testedCombos.AddRange(b.TestedCombos);
                }
                else if (b.Findings?.PerParameter != null && b.PhasesFound?.Count > 0)
                {
                    // Synthetic combos from Claude quantization batches that have findings
                    // but no explicit TestedCombos (imported via AppendBatchSummary).
                    // For each parameter in findings, mark it as tested for emitterTargeted
                    // in every phase that was found in this batch.
                    foreach (var paramName in b.Findings.PerParameter.Keys)
                    {
                        foreach (var phase in b.PhasesFound)
                        {
                            // Try to find the baseline from the phase emitters if available
                            float baseline = 0f;
                            if (phaseEmitters.TryGetValue(phase, out var emitters))
                            {
                                var em = emitters.FirstOrDefault(e => e.Index == b.EmitterTargeted);
                                if (em != null)
                                {
                                    baseline = paramName == "scaleStart"
                                        ? em.ScaleStart
                                        : em.TrackValues.GetValueOrDefault(paramName) ?? 0f;
                                }
                            }

                            testedCombos.Add(new TestedCombo
                            {
                                Phase = phase,
                                EmitterIndex = b.EmitterTargeted,
                                Parameter = paramName,
                                Baseline = baseline
                            });
                        }
                    }
                    _logger.LogInformation(
                        "SpellDNA: Synthesized {Count} TestedCombos from batch {Id} findings for {Spell}",
                        b.Findings.PerParameter.Count * b.PhasesFound.Count, b.BatchId, sourceSpellName);
                }
            }
        }

        // ── Step 2: For each param, find the best untested emitter per phase ──
        var paramPhaseTargets = new Dictionary<string, Dictionary<string, EmitterSnapshot>>();
        var paramReasons = new Dictionary<string, string>();
        int totalNovel = 0;

        foreach (var extremeDef in ExtremeParams)
        {
            var phaseTargets = new Dictionary<string, EmitterSnapshot>();
            int novelCount = 0;

            foreach (var (phase, emitters) in activePhases)
            {
                // If manual override, use it
                if (targetEmitters != null && targetEmitters.TryGetValue(phase, out int specified))
                {
                    var target = emitters.FirstOrDefault(e => e.Index == specified);
                    if (target != null) phaseTargets[phase] = target;
                    continue;
                }

                // Find untested emitters for this param+phase combo
                var candidates = new List<(EmitterSnapshot emitter, float baseline, float novelty)>();

                foreach (var emitter in emitters)
                {
                    float? baseline = extremeDef.ParamName switch
                    {
                        "scaleStart" => emitter.ScaleStart,
                        _ => emitter.TrackValues.GetValueOrDefault(extremeDef.ParamName)
                    };
                    if (!baseline.HasValue) continue;

                    // Check if this baseline was already tested for this param
                    bool alreadyTested = testedCombos.Any(tc =>
                        tc.Phase == phase &&
                        tc.Parameter == extremeDef.ParamName &&
                        IsBaselineSimilar(tc.Baseline, baseline.Value));

                    if (!alreadyTested)
                    {
                        // Novelty = how different this baseline is from all tested baselines
                        float novelty = ComputeNovelty(baseline.Value, extremeDef.ParamName, phase, testedCombos);
                        candidates.Add((emitter, baseline.Value, novelty));
                    }
                }

                if (candidates.Count > 0)
                {
                    // Pick the most novel candidate
                    var best = candidates.OrderByDescending(c => c.novelty).First();
                    phaseTargets[phase] = best.emitter;
                    novelCount++;
                }
                else
                {
                    // All emitters tested for this param in this phase — pick highest-rate as fallback
                    // but mark as not novel (won't affect clone count)
                    var fallback = emitters
                        .OrderByDescending(e => e.TrackValues.GetValueOrDefault("emissionRate") ?? 0)
                        .First();
                    phaseTargets[phase] = fallback;
                }
            }

            if (phaseTargets.Count > 0)
            {
                paramPhaseTargets[extremeDef.ParamName] = phaseTargets;
                if (novelCount > 0)
                {
                    totalNovel++;
                    paramReasons[extremeDef.ParamName] = $"{novelCount} phase(s) with untested emitters";
                }
                else
                {
                    paramReasons[extremeDef.ParamName] = "all baselines already tested (re-run)";
                }
            }
        }

        // ── Step 3: Build clone plans — only for params with novel combos ──
        int cloneNum = 0;
        var reasonParts = new List<string>();

        foreach (var extremeDef in ExtremeParams)
        {
            if (!paramPhaseTargets.TryGetValue(extremeDef.ParamName, out var phaseTargets))
                continue;

            // Skip params where all baselines were already tested
            bool hasNovel = paramReasons.TryGetValue(extremeDef.ParamName, out var reason)
                && !reason.Contains("already tested");

            if (!hasNovel) continue;

            // Clone A: SUPPRESS
            cloneNum++;
            var suppressPlan = BuildSmartExtremePlan(
                cloneNum, sourceSpellName, extremeDef, phaseTargets, isSuppress: true);
            if (suppressPlan != null) batch.Plans.Add(suppressPlan);

            // Clone B: CRANK
            cloneNum++;
            var crankPlan = BuildSmartExtremePlan(
                cloneNum, sourceSpellName, extremeDef, phaseTargets, isSuppress: false);
            if (crankPlan != null) batch.Plans.Add(crankPlan);

            reasonParts.Add($"{extremeDef.ParamName}: {reason}");
        }

        // If nothing novel remains, report it
        if (batch.Plans.Count == 0)
        {
            batch.EmitterReason = "All parameter/emitter combos tested for this spell. Try a different spell type.";
        }
        else
        {
            batch.EmitterReason = $"{batch.Plans.Count} clones ({batch.Plans.Count / 2} novel params). " +
                string.Join("; ", reasonParts.Take(4));
            if (reasonParts.Count > 4)
                batch.EmitterReason += $"; +{reasonParts.Count - 4} more";
        }

        batch.TargetedEmitterIndex = -1; // no single emitter — per-phase targeting
        batch.TotalObservations = batch.Plans.Sum(p => p.PhaseChanges.Count);
        return batch;
    }

    /// <summary>Are two baselines similar enough to skip? Within 10% or both near zero.</summary>
    private static bool IsBaselineSimilar(float tested, float candidate)
    {
        if (Math.Abs(tested) < 0.001f && Math.Abs(candidate) < 0.001f) return true;
        if (Math.Abs(tested) < 0.001f || Math.Abs(candidate) < 0.001f) return false;
        float ratio = candidate / tested;
        return ratio > 0.9f && ratio < 1.1f;
    }

    /// <summary>
    /// How "novel" is this baseline compared to already-tested baselines for the same param+phase?
    /// Higher = more different = more worth testing.
    /// </summary>
    private static float ComputeNovelty(float baseline, string paramName, string phase, List<TestedCombo> tested)
    {
        var relevant = tested.Where(tc => tc.Phase == phase && tc.Parameter == paramName).ToList();
        if (relevant.Count == 0) return 1.0f; // never tested = max novelty

        // Minimum distance from any tested baseline (normalized)
        float minDist = relevant.Min(tc =>
        {
            if (Math.Abs(tc.Baseline) < 0.001f && Math.Abs(baseline) < 0.001f) return 0f;
            if (Math.Abs(tc.Baseline) < 0.001f) return Math.Abs(baseline);
            return Math.Abs(baseline - tc.Baseline) / Math.Max(Math.Abs(tc.Baseline), 0.001f);
        });

        return minDist;
    }

    private ExperimentPlan? BuildSmartExtremePlan(
        int cloneIndex,
        string sourceSpellName,
        ExtremeParamDef extremeDef,
        Dictionary<string, EmitterSnapshot> phaseTargets,
        bool isSuppress)
    {
        var plan = new ExperimentPlan
        {
            CloneIndex = cloneIndex,
            PhaseChanges = new Dictionary<string, ExperimentPhaseChange>()
        };

        string label = isSuppress ? extremeDef.SuppressLabel : extremeDef.CrankLabel;
        var descParts = new List<string>();

        foreach (var (phase, emitter) in phaseTargets)
        {
            float? baseline = extremeDef.ParamName switch
            {
                "scaleStart" => emitter.ScaleStart,
                _ => emitter.TrackValues.GetValueOrDefault(extremeDef.ParamName)
            };

            if (!baseline.HasValue) continue;

            float newValue;
            string mode;

            if (extremeDef.Mode == SweepMode.Multiply)
            {
                float mult = isSuppress ? extremeDef.SuppressMultiplier : extremeDef.CrankMultiplier;
                newValue = baseline.Value * mult;
                mode = $"×{mult}";
            }
            else
            {
                newValue = isSuppress ? extremeDef.SuppressAbsolute : extremeDef.CrankAbsolute;
                mode = $"={newValue}";
            }

            if (Math.Abs(newValue - baseline.Value) < 0.001f) continue;
            newValue = MathF.Round(newValue, 3);

            plan.PhaseChanges[phase] = new ExperimentPhaseChange
            {
                Phase = phase,
                EmitterIndex = emitter.Index,
                Parameter = extremeDef.ParamName,
                BaselineValue = baseline.Value,
                NewValue = newValue,
                Mode = mode,
                ParameterDescription = extremeDef.Description,
            };

            descParts.Add($"{phase}/em{emitter.Index}: {extremeDef.ParamName} {baseline.Value:F2}→{newValue:F2}");
        }

        if (descParts.Count == 0) return null;

        plan.SpellName = $"{sourceSpellName} X{cloneIndex:D2}";
        plan.SpellDescription = $"[EXTREME #{cloneIndex} {label}] {string.Join(" | ", descParts)}";

        return plan;
    }

    /// <summary>
    /// Record observations from an experiment batch.
    /// Takes the clone index and observation notes, merges them into
    /// the archetype knowledge base.
    ///
    /// This is the "quantize" step — raw observations become archetype
    /// refinements and parameter significance notes.
    /// </summary>
    public void RecordObservation(ExperimentObservation observation)
    {
        // Load or create the experiment log
        string logPath = Path.Combine(BasePath, "experiment_log.json");
        var log = new List<ExperimentObservation>();

        if (File.Exists(logPath))
        {
            try
            {
                log = JsonSerializer.Deserialize<List<ExperimentObservation>>(
                    File.ReadAllText(logPath), _jsonOpts) ?? new();
            }
            catch { /* start fresh */ }
        }

        observation.RecordedAt = DateTime.UtcNow;
        log.Add(observation);

        File.WriteAllText(logPath, JsonSerializer.Serialize(log, _jsonOpts));
        _logger.LogInformation("SpellDNA: Recorded observation for {Spell} clone #{Clone}",
            observation.SourceSpellName, observation.CloneIndex);
    }

    /// <summary>Get all recorded observations.</summary>
    public List<ExperimentObservation> GetObservations()
    {
        string logPath = Path.Combine(BasePath, "experiment_log.json");
        if (!File.Exists(logPath)) return new();

        try
        {
            return JsonSerializer.Deserialize<List<ExperimentObservation>>(
                File.ReadAllText(logPath), _jsonOpts) ?? new();
        }
        catch { return new(); }
    }



    private void PersistArchetypes()
    {
        var list = _archetypes.Values.ToList();
        File.WriteAllText(ArchetypesPath, JsonSerializer.Serialize(list, _jsonOpts));
    }

    private static string SanitizeFilename(string name)
    {
        var invalid = Path.GetInvalidFileNameChars();
        return new string(name.Select(c => invalid.Contains(c) ? '_' : c).ToArray())
            .Replace(' ', '_');
    }

    private static readonly JsonSerializerOptions _jsonOpts = new()
    {
        WriteIndented = true,
        PropertyNamingPolicy = JsonNamingPolicy.CamelCase,
        DefaultIgnoreCondition = JsonIgnoreCondition.WhenWritingNull,
        Converters = { new JsonStringEnumConverter(JsonNamingPolicy.CamelCase) }
    };
}


