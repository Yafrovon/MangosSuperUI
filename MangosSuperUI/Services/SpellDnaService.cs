/// <summary>
/// A named emitter behavior pattern.
///
/// An archetype captures: "when you see these parameter ranges together,
/// the visual result is [X], and here's why each parameter matters."
///
/// Archetypes are the vocabulary of spell visual design. Instead of
/// thinking in raw numbers (rate=40, life=0.7, speed=0.194), you think
/// in behavior names: "upward_drift", "fast_scatter", "imploding_gather".
/// </summary>
public class EmitterArchetype
{
    /// <summary>Machine-readable ID, e.g. "upward_drift", "fast_scatter".</summary>
    public string Id { get; set; } = "";

    /// <summary>Human-readable name, e.g. "Upward Drift".</summary>
    public string Name { get; set; } = "";

    /// <summary>
    /// Detailed explanation of what this behavior looks like and why
    /// the parameter combination produces it. This is the knowledge.
    /// </summary>
    public string Description { get; set; } = "";

    /// <summary>One-line description of the visible result.</summary>
    public string VisualResult { get; set; } = "";

    /// <summary>Known vanilla spells that exhibit this behavior.</summary>
    public List<string> ExampleSpells { get; set; } = new();

    /// <summary>
    /// The parameter fingerprint that defines this archetype.
    /// Each entry specifies a parameter name, acceptable range (min/max),
    /// and a human-readable explanation of why this range matters.
    /// </summary>
    public List<ParamFingerprint> Fingerprint { get; set; } = new();

    /// <summary>
    /// If set, only emitters of this type can match this archetype.
    /// 0=point, 1=sphere, 2=plane, 3=spline. Null = any type.
    /// </summary>
    public byte? RequiredEmitterType { get; set; }

    /// <summary>When this archetype was last updated.</summary>
    public DateTime? LastUpdated { get; set; }
}

/// <summary>
/// One parameter range in an archetype's fingerprint.
/// Captures not just the range, but WHY this range matters
/// for producing the archetype's visual behavior.
/// </summary>
public class ParamFingerprint
{
    /// <summary>
    /// Parameter name — matches M2EmitterParser property names:
    /// "emissionRate", "lifespan", "emissionSpeed", "gravity",
    /// "emissionAreaLength", "emissionAreaWidth", "verticalRange",
    /// "horizontalRange", "speedVariation", "scaleStart", "scaleMid",
    /// "scaleEnd", "blendMode"
    /// </summary>
    public string Parameter { get; set; } = "";

    /// <summary>Minimum value for this parameter in this archetype.</summary>
    public float Min { get; set; }

    /// <summary>Maximum value for this parameter in this archetype.</summary>
    public float Max { get; set; }

    /// <summary>
    /// Why does this parameter value matter for this archetype?
    /// e.g. "Low speed = particles barely move on their own, so gravity dominates"
    /// This is the knowledge capture field.
    /// </summary>
    public string? Significance { get; set; }
}

/// <summary>Result of classifying an emitter against archetypes.</summary>
public class ArchetypeMatch
{
    public string ArchetypeId { get; set; } = "";
    public string ArchetypeName { get; set; } = "";

    /// <summary>Match score 0-1. Above 0.3 = plausible, above 0.7 = strong match.</summary>
    public float Score { get; set; }
}


