    /// <summary>Load experiment summary for a spell. Returns null if none exists.</summary>
    public ExperimentSummary? GetSummary(string spellName)
    {
        string path = Path.Combine(SummariesDir, SanitizeFilename(spellName) + ".json");
        if (!File.Exists(path)) return null;
        try
        {
            return JsonSerializer.Deserialize<ExperimentSummary>(File.ReadAllText(path), _jsonOpts);
        }
        catch { return null; }
    }

    /// <summary>Save or update an experiment summary.</summary>
    public void SaveSummary(ExperimentSummary summary)
    {
        Directory.CreateDirectory(SummariesDir);
        string path = Path.Combine(SummariesDir, SanitizeFilename(summary.SpellName) + ".json");
        File.WriteAllText(path, JsonSerializer.Serialize(summary, _jsonOpts));
        _logger.LogInformation("SpellDNA: Saved experiment summary for {Spell} ({Batches} batches)",
            summary.SpellName, summary.Batches.Count);
    }

    /// <summary>List all spell names that have experiment summaries.</summary>
    public List<string> ListSummaries()
    {
        if (!Directory.Exists(SummariesDir)) return new();
        return Directory.GetFiles(SummariesDir, "*.json")
            .Select(f => Path.GetFileNameWithoutExtension(f))
            .ToList();
    }

    /// <summary>
    /// Determine which emitter index to target next for a spell, based on
    /// what's already been tested. Returns the emitter index and the reason.
    /// </summary>
    public (int emitterIndex, string reason) PickNextEmitter(
        string spellName,
        List<EmitterSnapshot> phaseEmitters)
    {
        var summary = GetSummary(spellName);
        if (summary == null || summary.Batches.Count == 0)
        {
            // First batch: pick highest-rate emitter
            var best = phaseEmitters
                .OrderByDescending(e => e.TrackValues.GetValueOrDefault("emissionRate") ?? 0)
                .First();
            return (best.Index, $"First batch — highest emission rate (emitter {best.Index})");
        }

        // Find which emitter indices have already been tested
        var testedEmitters = summary.Batches
            .Select(b => b.EmitterTargeted)
            .ToHashSet();

        // All available emitter indices
        var allIndices = phaseEmitters.Select(e => e.Index).Distinct().OrderBy(i => i).ToList();

        // Find untested emitters
        var untested = allIndices.Where(i => !testedEmitters.Contains(i)).ToList();

        if (untested.Count == 0)
        {
            // All emitters tested — suggest re-running highest-impact one or different spell
            return (allIndices[0], $"All {allIndices.Count} emitters already tested. Re-running emitter 0 or try a different spell.");
        }

        // Pick next untested, prefer by emission rate
        var nextEmitter = phaseEmitters
            .Where(e => untested.Contains(e.Index))
            .OrderByDescending(e => e.TrackValues.GetValueOrDefault("emissionRate") ?? 0)
            .First();

        return (nextEmitter.Index, $"Batch {summary.Batches.Count + 1} — emitter {nextEmitter.Index} (untested, {testedEmitters.Count}/{allIndices.Count} done)");
    }

