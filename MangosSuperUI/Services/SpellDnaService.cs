/// <summary>
/// Accumulated experiment knowledge for one spell.
/// Tracks which emitters/parameters have been tested and what was learned.
/// Used by PlanExtremeExperiments to auto-advance to untested emitters.
/// Stored at /opt/mangossuperui/spelldna/summaries/{SpellName}.json
/// </summary>
public class ExperimentSummary
{
    public string SpellName { get; set; } = "";
    public int SourceEntry { get; set; }

    /// <summary>Completed experiment batches with findings.</summary>
    public List<BatchSummary> Batches { get; set; } = new();

    /// <summary>Emitter indices not yet tested.</summary>
    public List<int> EmittersRemaining { get; set; } = new();

    /// <summary>Suggestion for what to test next.</summary>
    public string? NextBatchSuggestion { get; set; }
}

/// <summary>Summary of one completed experiment batch.</summary>
public class BatchSummary
{
    public int BatchId { get; set; }
    public string Mode { get; set; } = "extreme";
    public string Date { get; set; } = "";

    /// <summary>Which emitter index was targeted in this batch (legacy — use TestedCombos for per-phase).</summary>
    public int EmitterTargeted { get; set; }

    /// <summary>Why this emitter was selected.</summary>
    public string? EmitterSelectionReason { get; set; }

    /// <summary>Which phases had emitters to test.</summary>
    public List<string> PhasesFound { get; set; } = new();

    /// <summary>Which parameters were covered in this batch.</summary>
    public List<string> ParametersCovered { get; set; } = new();

    /// <summary>
    /// Per-phase, per-emitter, per-parameter combos tested in this batch.
    /// Used to skip redundant tests in future batches.
    /// </summary>
    public List<TestedCombo>? TestedCombos { get; set; }

    /// <summary>Synthesized findings from this batch.</summary>
    public BatchFindings? Findings { get; set; }
}

/// <summary>
/// Records one tested (phase, emitter, parameter, baseline) combination.
/// Two combos with the same parameter and similar baselines (within 10%)
/// are considered equivalent — no need to re-test.
/// </summary>
public class TestedCombo
{
    public string Phase { get; set; } = "";
    public int EmitterIndex { get; set; }
    public string Parameter { get; set; } = "";
    public float Baseline { get; set; }
}

/// <summary>Synthesized findings from one experiment batch.</summary>
public class BatchFindings
{
    /// <summary>Parameters ranked from most to least visual impact.</summary>
    public List<string> ParameterRanking { get; set; } = new();

    /// <summary>Per-parameter findings.</summary>
    public Dictionary<string, ParameterFinding> PerParameter { get; set; } = new();
}

/// <summary>What we learned about one parameter from an experiment batch.</summary>
public class ParameterFinding
{
    public float SuppressValue { get; set; }
    public float CrankValue { get; set; }
    public string SuppressImpact { get; set; } = "";
    public string CrankImpact { get; set; } = "";
    public string? Notes { get; set; }
}