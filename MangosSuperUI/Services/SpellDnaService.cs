    /// <summary>
    /// Plan an experiment batch for a source spell.
    ///
    /// For each phase, picks a target emitter (the most "interesting" one —
    /// highest emission rate, or the one with most visual impact).
    ///
    /// Generates N experiment plans where each plan changes one parameter
    /// per phase. The plans cycle through parameters and test values so
    /// the full batch covers the parameter space systematically.
    ///
    /// Returns the plans — caller (PatchController) executes them by
    /// cloning spells and patching M2s.
    /// </summary>
    /// <param name="phaseEmitters">Per-phase emitter snapshots from M2EmitterParser.ReadEmitters()</param>
    /// <param name="sourceSpellEntry">Source spell entry ID</param>
    /// <param name="sourceSpellName">Source spell name for labeling</param>
    /// <param name="batchSize">How many clones to generate (default 20)</param>
    /// <param name="targetEmitters">Optional: specific emitter index to target per phase.
    /// If null, auto-selects the highest-rate emitter per phase.</param>
    public ExperimentBatch PlanExperiments(
        Dictionary<string, List<EmitterSnapshot>> phaseEmitters,
        int sourceSpellEntry,
        string sourceSpellName,
        int batchSize = 20,
        Dictionary<string, int>? targetEmitters = null)
    {
        var batch = new ExperimentBatch
        {
            SourceSpellEntry = sourceSpellEntry,
            SourceSpellName = sourceSpellName,
            CreatedAt = DateTime.UtcNow,
            Plans = new List<ExperimentPlan>()
        };

        // Determine which phases have emitters to work with
        var activePhases = phaseEmitters
            .Where(kv => kv.Value.Count > 0)
            .ToDictionary(kv => kv.Key, kv => kv.Value);

        if (activePhases.Count == 0) return batch;

        // Pick target emitter per phase
        var targets = new Dictionary<string, EmitterSnapshot>();
        foreach (var (phase, emitters) in activePhases)
        {
            int targetIdx = 0;
            if (targetEmitters != null && targetEmitters.TryGetValue(phase, out int specified))
            {
                targetIdx = specified;
            }
            else
            {
                // Auto-select: pick the emitter with highest emission rate
                // (most visually prominent, changes will be most visible)
                var best = emitters
                    .OrderByDescending(e => e.TrackValues.GetValueOrDefault("emissionRate") ?? 0)
                    .First();
                targetIdx = best.Index;
            }

            var target = emitters.FirstOrDefault(e => e.Index == targetIdx);
            if (target != null)
                targets[phase] = target;
        }

        if (targets.Count == 0) return batch;

        // Build the sweep schedule: distribute parameters across clones
        // so each clone varies a different param per phase
        var phaseKeys = targets.Keys.OrderBy(k => k).ToList();
        int paramCount = SweepParams.Length;

        // For each clone, assign one parameter+value per phase
        // Phase 0 gets param[cloneIdx % paramCount], value[cloneIdx / paramCount % valueCount]
        // Phase 1 gets a different param offset so phases don't repeat the same param
        for (int cloneIdx = 0; cloneIdx < batchSize; cloneIdx++)
        {
            var plan = new ExperimentPlan
            {
                CloneIndex = cloneIdx + 1,
                PhaseChanges = new Dictionary<string, ExperimentPhaseChange>()
            };

            var descParts = new List<string>();

            for (int phaseOrdinal = 0; phaseOrdinal < phaseKeys.Count; phaseOrdinal++)
            {
                string phase = phaseKeys[phaseOrdinal];
                var emitter = targets[phase];

                // Offset the parameter selection per phase so we get variety
                int paramIdx = (cloneIdx + phaseOrdinal) % paramCount;
                var sweepDef = SweepParams[paramIdx];

                // Cycle through test values for this parameter
                int valueIdx = ((cloneIdx + phaseOrdinal) / paramCount) % sweepDef.TestValues.Length;
                float testValue = sweepDef.TestValues[valueIdx];

                // Get baseline value
                float? baseline = sweepDef.ParamName switch
                {
                    "scaleStart" => emitter.ScaleStart,
                    _ => emitter.TrackValues.GetValueOrDefault(sweepDef.ParamName)
                };

                if (!baseline.HasValue) continue;

                // Compute new value
                float newValue = sweepDef.Mode == SweepMode.Multiply
                    ? baseline.Value * testValue
                    : testValue;

                // Skip if the change is negligible
                if (Math.Abs(newValue - baseline.Value) < 0.001f) continue;

                // Round for readability
                newValue = MathF.Round(newValue, 3);

                var change = new ExperimentPhaseChange
                {
                    Phase = phase,
                    EmitterIndex = emitter.Index,
                    Parameter = sweepDef.ParamName,
                    BaselineValue = baseline.Value,
                    NewValue = newValue,
                    Mode = sweepDef.Mode == SweepMode.Multiply
                        ? $"×{testValue}"
                        : $"={testValue}",
                    ParameterDescription = sweepDef.Description,
                };

                plan.PhaseChanges[phase] = change;
                descParts.Add($"{phase}/em{emitter.Index}: {sweepDef.ParamName} {baseline.Value:F2}→{newValue:F2}");
            }

            if (descParts.Count == 0) continue;

            // Build the description that goes into spell_template.description
            plan.SpellDescription = $"[EXP #{cloneIdx + 1}] {string.Join(" | ", descParts)}";
            plan.SpellName = $"{sourceSpellName} E{cloneIdx + 1:D2}";

            batch.Plans.Add(plan);
        }

        batch.TotalObservations = batch.Plans.Sum(p => p.PhaseChanges.Count);
        return batch;
    }

