using System.Text.Json;
using System.Text.Json.Serialization;

namespace MangosSuperUI.Services;

/// <summary>
/// Session 30: Spell Visual Knowledge Map
///
/// PURPOSE: Build and maintain a structured understanding of how spell visuals
/// work at the composition level. This is the "why" layer that sits between
/// raw M2 data and human intent.
///
/// THREE TIERS OF KNOWLEDGE:
///
/// ┌─────────────────────────────────────────────────────────────────────┐
/// │  Tier 1: EMITTER ARCHETYPES (the vocabulary)                       │
/// │                                                                     │
/// │  Named behavior patterns with parameter fingerprints.               │
/// │  Built empirically — we observe a pattern, name it, record the     │
/// │  parameter ranges that produce it.                                  │
/// │                                                                     │
/// │  Examples:                                                          │
/// │  • "upward_drift" — low speed, strong negative gravity, long life  │
/// │  • "imploding_gather" — negative speed, wide area, zero gravity    │
/// │  • "fast_scatter" — high speed, short life, wide angular range     │
/// │  • "dense_flicker" — high rate, very short life, tiny area         │
/// │  • "persistent_field" — long life, low speed, wide area            │
/// │  • "radial_burst" — plane emitter, wide area, short life           │
/// │                                                                     │
/// │  Each archetype defines what visual effect the emitter produces     │
/// │  and WHY those parameter values create that effect.                 │
/// ├─────────────────────────────────────────────────────────────────────┤
/// │  Tier 2: M2 PROFILES (the catalog)                                  │
/// │                                                                     │
/// │  A complete structural read of one M2 file: emitter count,          │
/// │  texture count, per-emitter archetype classification, per-texture   │
/// │  role assignment, and the emitter→texture wiring map.               │
/// │                                                                     │
/// │  This answers: "What does Fire_Precast_Hand.m2 actually do?"        │
/// │  → 5 emitters: [upward_drift, imploding_gather, glow_pulse,        │
/// │     glow_pulse, imploding_gather_wide]. 5 textures: [atlas_cloud,  │
/// │     body_core, glow_soft, body_mass, ribbon_trail].                 │
/// │  → Emitter 0 uses texture 2 (glow) + archetype upward_drift        │
/// │     = "rising glowing sparks"                                       │
/// │  → Emitter 1 uses texture 1 (body) + archetype imploding_gather    │
/// │     = "energy condensing into a ball"                               │
/// │                                                                     │
/// │  Profiles are auto-generated by scanning M2 bytes, then             │
/// │  manually enriched with archetype tags and descriptions after       │
/// │  in-game observation.                                               │
/// ├─────────────────────────────────────────────────────────────────────┤
/// │  Tier 3: SPELL DNA (the intent document)                            │
/// │                                                                     │
/// │  Maps human-readable creative intent to concrete M2+texture+emitter│
/// │  configuration. Each spell has a DNA document that says:            │
/// │                                                                     │
/// │  "I want [visual goal] → use [archetype] on emitter [N] with       │
/// │   texture role [X] because [why this combination works]"            │
/// │                                                                     │
/// │  The DNA document can COMPILE into a SpellTuningPreset JSON —       │
/// │  it's the design spec that the tuning system executes.              │
/// │                                                                     │
/// │  Every parameter value has a "why" annotation. Every time we        │
/// │  tune and observe, we update the why. This is how knowledge         │
/// │  accumulates across sessions.                                       │
/// └─────────────────────────────────────────────────────────────────────┘
///
/// STORAGE:
///   Archetypes:  /opt/mangossuperui/spelldna/archetypes.json
///   M2 Profiles: /opt/mangossuperui/spelldna/profiles/{filename}.json
///   Spell DNAs:  /opt/mangossuperui/spelldna/spells/{spellName}.json
///
/// DEPENDENCIES:
///   M2EmitterParser — reads emitter data from M2 bytes (read-only usage)
///   M2TextureParser — reads texture table from M2 bytes (read-only usage)
///   IConfiguration  — for base paths
///   ILogger         — diagnostics
///
/// This service does NOT modify M2 files, build patches, or touch DBC/SQL.
/// It is purely a knowledge store and classification engine.
/// </summary>
public class SpellDnaService
{
    private readonly IConfiguration _config;
    private readonly ILogger<SpellDnaService> _logger;

    // In-memory caches — loaded from disk at startup
    private readonly Dictionary<string, EmitterArchetype> _archetypes = new(StringComparer.OrdinalIgnoreCase);
    private readonly Dictionary<string, M2Profile> _profiles = new(StringComparer.OrdinalIgnoreCase);
    private readonly Dictionary<string, SpellDna> _spellDnas = new(StringComparer.OrdinalIgnoreCase);

    private string BasePath => _config["SpellCreator:DnaPath"]
        ?? "/opt/mangossuperui/spelldna";
    private string ArchetypesPath => Path.Combine(BasePath, "archetypes.json");
    private string ProfilesDir => Path.Combine(BasePath, "profiles");
    private string SpellDnaDir => Path.Combine(BasePath, "spells");
    private string SummariesDir => Path.Combine(BasePath, "summaries");

    public SpellDnaService(IConfiguration config, ILogger<SpellDnaService> logger)
    {
        _config = config;
        _logger = logger;
        LoadAll();
    }

    // ═══════════════════════════════════════════════════════════════
    //  STARTUP: Load everything from disk
    // ═══════════════════════════════════════════════════════════════

    private void LoadAll()
    {
        Directory.CreateDirectory(BasePath);
        Directory.CreateDirectory(ProfilesDir);
        Directory.CreateDirectory(SpellDnaDir);
        Directory.CreateDirectory(SummariesDir);

        LoadArchetypes();
        LoadProfiles();
        LoadSpellDnas();
    }

    private void LoadArchetypes()
    {
        if (!File.Exists(ArchetypesPath))
        {
            // Seed with initial archetypes derived from Fireball analysis
            SeedDefaultArchetypes();
            return;
        }

        try
        {
            var json = File.ReadAllText(ArchetypesPath);
            var list = JsonSerializer.Deserialize<List<EmitterArchetype>>(json, _jsonOpts);
            if (list != null)
            {
                foreach (var a in list)
                    _archetypes[a.Id] = a;
            }
            _logger.LogInformation("SpellDNA: Loaded {Count} archetypes", _archetypes.Count);
        }
        catch (Exception ex)
        {
            _logger.LogWarning(ex, "SpellDNA: Failed to load archetypes, seeding defaults");
            SeedDefaultArchetypes();
        }
    }

    private void LoadProfiles()
    {
        if (!Directory.Exists(ProfilesDir)) return;

        foreach (var file in Directory.GetFiles(ProfilesDir, "*.json"))
        {
            try
            {
                var json = File.ReadAllText(file);
                var profile = JsonSerializer.Deserialize<M2Profile>(json, _jsonOpts);
                if (profile != null && !string.IsNullOrEmpty(profile.M2Filename))
                {
                    _profiles[profile.M2Filename] = profile;
                }
            }
            catch (Exception ex)
            {
                _logger.LogWarning(ex, "SpellDNA: Failed to load profile {File}", Path.GetFileName(file));
            }
        }
        _logger.LogInformation("SpellDNA: Loaded {Count} M2 profiles", _profiles.Count);
    }

    private void LoadSpellDnas()
    {
        if (!Directory.Exists(SpellDnaDir)) return;

        foreach (var file in Directory.GetFiles(SpellDnaDir, "*.json"))
        {
            try
            {
                var json = File.ReadAllText(file);
                var dna = JsonSerializer.Deserialize<SpellDna>(json, _jsonOpts);
                if (dna != null && !string.IsNullOrEmpty(dna.SpellName))
                {
                    _spellDnas[dna.SpellName] = dna;
                }
            }
            catch (Exception ex)
            {
                _logger.LogWarning(ex, "SpellDNA: Failed to load spell DNA {File}", Path.GetFileName(file));
            }
        }
        _logger.LogInformation("SpellDNA: Loaded {Count} spell DNAs", _spellDnas.Count);
    }

    // ═══════════════════════════════════════════════════════════════
    //  TIER 1: EMITTER ARCHETYPES
    // ═══════════════════════════════════════════════════════════════

    /// <summary>Get all known archetypes.</summary>
    public IReadOnlyDictionary<string, EmitterArchetype> GetArchetypes() => _archetypes;

    /// <summary>Get a specific archetype by ID.</summary>
    public EmitterArchetype? GetArchetype(string id)
        => _archetypes.TryGetValue(id, out var a) ? a : null;

    /// <summary>Add or update an archetype.</summary>
    public void SaveArchetype(EmitterArchetype archetype)
    {
        _archetypes[archetype.Id] = archetype;
        PersistArchetypes();
        _logger.LogInformation("SpellDNA: Saved archetype '{Id}'", archetype.Id);
    }

    /// <summary>Delete an archetype.</summary>
    public bool RemoveArchetype(string id)
    {
        if (!_archetypes.Remove(id)) return false;
        PersistArchetypes();
        return true;
    }

    /// <summary>
    /// Classify an emitter snapshot against known archetypes.
    /// Returns the best-matching archetype ID and a confidence score (0-1).
    /// Returns null if no archetype matches above the minimum threshold.
    /// </summary>
    public ArchetypeMatch? ClassifyEmitter(EmitterSnapshot emitter)
    {
        if (_archetypes.Count == 0) return null;

        ArchetypeMatch? best = null;

        foreach (var archetype in _archetypes.Values)
        {
            float score = ScoreArchetypeMatch(emitter, archetype);
            if (score > 0.3f && (best == null || score > best.Score))
            {
                best = new ArchetypeMatch
                {
                    ArchetypeId = archetype.Id,
                    ArchetypeName = archetype.Name,
                    Score = score
                };
            }
        }

        return best;
    }

    /// <summary>
    /// Score how well an emitter matches an archetype (0-1).
    ///
    /// For each parameter the archetype defines a range, we check whether
    /// the emitter's value falls within that range. Score = fraction of
    /// defined parameters that match.
    ///
    /// Special handling:
    ///   - Negative speed (imploding) vs positive speed (exploding) is a
    ///     categorical distinction, not just a range difference.
    ///   - EmitterType (point/sphere/plane) is a hard constraint when specified.
    ///   - BlendMode match is weighted but not disqualifying.
    /// </summary>
    private float ScoreArchetypeMatch(EmitterSnapshot emitter, EmitterArchetype archetype)
    {
        int checks = 0;
        int matches = 0;

        // Hard constraints — if these fail, score is 0
        if (archetype.RequiredEmitterType.HasValue)
        {
            if (emitter.EmitterType != archetype.RequiredEmitterType.Value)
                return 0f;
        }

        // Check each parameter fingerprint
        foreach (var fp in archetype.Fingerprint)
        {
            float? emitterValue = fp.Parameter switch
            {
                "emissionRate" => emitter.TrackValues.GetValueOrDefault("emissionRate"),
                "lifespan" => emitter.TrackValues.GetValueOrDefault("lifespan"),
                "emissionSpeed" => emitter.TrackValues.GetValueOrDefault("emissionSpeed"),
                "gravity" => emitter.TrackValues.GetValueOrDefault("gravity"),
                "emissionAreaLength" => emitter.TrackValues.GetValueOrDefault("emissionAreaLength"),
                "emissionAreaWidth" => emitter.TrackValues.GetValueOrDefault("emissionAreaWidth"),
                "verticalRange" => emitter.TrackValues.GetValueOrDefault("verticalRange"),
                "horizontalRange" => emitter.TrackValues.GetValueOrDefault("horizontalRange"),
                "speedVariation" => emitter.TrackValues.GetValueOrDefault("speedVariation"),
                "scaleStart" => emitter.ScaleStart,
                "scaleMid" => emitter.ScaleMid,
                "scaleEnd" => emitter.ScaleEnd,
                "blendMode" => emitter.BlendMode,
                _ => null
            };

            if (!emitterValue.HasValue) continue;

            checks++;
            float val = emitterValue.Value;

            // Check if value falls in the fingerprint range
            if (val >= fp.Min && val <= fp.Max)
                matches++;
            // Partial credit for being close (within 50% of range width)
            else
            {
                float rangeWidth = fp.Max - fp.Min;
                if (rangeWidth > 0)
                {
                    float distance = val < fp.Min ? fp.Min - val : val - fp.Max;
                    float tolerance = rangeWidth * 0.5f;
                    if (distance < tolerance)
                        matches++; // close enough
                }
            }
        }

        if (checks == 0) return 0f;
        return (float)matches / checks;
    }

    // ═══════════════════════════════════════════════════════════════
    //  TIER 2: M2 PROFILES
    // ═══════════════════════════════════════════════════════════════

    /// <summary>Get all loaded M2 profiles.</summary>
    public IReadOnlyDictionary<string, M2Profile> GetProfiles() => _profiles;

    /// <summary>Get a profile by M2 filename (case-insensitive).</summary>
    public M2Profile? GetProfile(string m2Filename)
        => _profiles.TryGetValue(m2Filename, out var p) ? p : null;

    /// <summary>
    /// Build an M2 profile from raw M2 bytes.
    /// Reads emitters and textures, classifies each emitter against known
    /// archetypes, maps emitter→texture wiring.
    ///
    /// The profile is returned but NOT auto-saved — the caller decides
    /// whether to persist (allows preview before committing).
    /// </summary>
    public M2Profile BuildProfile(byte[] m2Data, string m2Filename, string? m2RelativePath = null)
    {
        var emitters = M2EmitterParser.ReadEmitters(m2Data);
        var textures = M2TextureParser.ParseTextures(m2Data);

        var profile = new M2Profile
        {
            M2Filename = Path.GetFileName(m2Filename),
            M2Path = m2RelativePath ?? m2Filename,
            ScannedAt = DateTime.UtcNow,
            EmitterCount = emitters.Count,
            TextureCount = textures.Count,
        };

        // Build texture entries
        for (int i = 0; i < textures.Count; i++)
        {
            profile.Textures.Add(new ProfileTexture
            {
                Index = i,
                VanillaFilename = textures[i].Filename,
                // Role/description can be enriched later from recipes or manual annotation
            });
        }

        // Build emitter entries with archetype classification
        foreach (var em in emitters)
        {
            var match = ClassifyEmitter(em);

            var profileEmitter = new ProfileEmitter
            {
                Index = em.Index,
                TextureId = em.TextureId,
                BlendMode = em.BlendMode,
                EmitterType = em.EmitterType,

                // Raw values — the ground truth
                EmissionRate = em.TrackValues.GetValueOrDefault("emissionRate"),
                Lifespan = em.TrackValues.GetValueOrDefault("lifespan"),
                EmissionSpeed = em.TrackValues.GetValueOrDefault("emissionSpeed"),
                Gravity = em.TrackValues.GetValueOrDefault("gravity"),
                EmissionAreaLength = em.TrackValues.GetValueOrDefault("emissionAreaLength"),
                EmissionAreaWidth = em.TrackValues.GetValueOrDefault("emissionAreaWidth"),
                VerticalRange = em.TrackValues.GetValueOrDefault("verticalRange"),
                HorizontalRange = em.TrackValues.GetValueOrDefault("horizontalRange"),
                SpeedVariation = em.TrackValues.GetValueOrDefault("speedVariation"),
                ScaleStart = em.ScaleStart,
                ScaleMid = em.ScaleMid,
                ScaleEnd = em.ScaleEnd,

                // Classification
                ArchetypeId = match?.ArchetypeId,
                ArchetypeScore = match?.Score,
            };

            profile.Emitters.Add(profileEmitter);
        }

        // Wire up reverse references: which emitters point to each texture
        foreach (var tex in profile.Textures)
        {
            tex.ReferencedByEmitters = profile.Emitters
                .Where(e => e.TextureId == tex.Index)
                .Select(e => e.Index)
                .ToList();
        }

        return profile;
    }

    /// <summary>Save an M2 profile to disk.</summary>
    public void SaveProfile(M2Profile profile)
    {
        _profiles[profile.M2Filename] = profile;

        string safeName = SanitizeFilename(profile.M2Filename);
        string path = Path.Combine(ProfilesDir, $"{safeName}.json");
        File.WriteAllText(path, JsonSerializer.Serialize(profile, _jsonOpts));

        _logger.LogInformation("SpellDNA: Saved profile for '{M2}'", profile.M2Filename);
    }

    /// <summary>
    /// Batch-scan M2 files from a directory.
    /// Reads each .m2 file, builds a profile, classifies emitters.
    /// Returns profiles for all M2s that have at least one emitter.
    /// Does NOT auto-save — returns the list for review.
    /// </summary>
    public List<M2Profile> ScanDirectory(string m2Directory, string? subdirectory = null)
    {
        var results = new List<M2Profile>();

        string scanDir = subdirectory != null
            ? Path.Combine(m2Directory, subdirectory)
            : m2Directory;

        if (!Directory.Exists(scanDir))
        {
            _logger.LogWarning("SpellDNA: Scan directory not found: {Dir}", scanDir);
            return results;
        }

        var m2Files = Directory.GetFiles(scanDir, "*.m2", SearchOption.AllDirectories);
        _logger.LogInformation("SpellDNA: Scanning {Count} M2 files in {Dir}", m2Files.Length, scanDir);

        foreach (var filePath in m2Files)
        {
            try
            {
                var data = File.ReadAllBytes(filePath);
                string relativePath = Path.GetRelativePath(m2Directory, filePath);
                var profile = BuildProfile(data, filePath, relativePath);

                // Only keep M2s with emitters (skip meshes, attachments, etc.)
                if (profile.EmitterCount > 0)
                    results.Add(profile);
            }
            catch (Exception ex)
            {
                _logger.LogWarning(ex, "SpellDNA: Failed to scan {File}", Path.GetFileName(filePath));
            }
        }

        _logger.LogInformation("SpellDNA: Scanned {Total} M2s, {WithEmitters} have emitters",
            m2Files.Length, results.Count);

        return results;
    }

    /// <summary>
    /// Save all profiles from a scan batch.
    /// </summary>
    public int SaveProfiles(List<M2Profile> profiles)
    {
        int saved = 0;
        foreach (var p in profiles)
        {
            SaveProfile(p);
            saved++;
        }
        return saved;
    }

